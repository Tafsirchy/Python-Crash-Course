name = " Tafsir  is a good boy He is a good student and he is a good friend"

print(name.replace("  ", " ")) # replace all occurrences of "  " with " "

# Strings are immutable in Python, which means that once a string is created, it cannot be changed. However, you can create a new string by replacing parts of the original string using the replace() method. In this case, we are replacing all occurrences of double spaces ("  ") with a single space (" ").